"""Evidence-grounded assistant: semantic retrieval + local generative answering.

The assistant is two layers that must not be confused with each other.

*Deterministic guardrails* decide what may be answered at all.  Current
environmental outlooks, case-count boundaries, clinical questions and
ambiguous scopes are handled by reviewed policy code; a language model is not
allowed to invent a forecast, a count, or an operational alert.

*Learned components* answer the questions that evidence can support.  Retrieval
is cross-lingual sentence embeddings (multilingual-E5) over the collected
records, so an English question finds Odia and Hindi evidence.  A live record
retains no source text, so what is embedded for it is its structured
provenance -- disease, district in three scripts, registered document title,
language, date -- and each answer reports which of the two it ranked.  The
answer is generated by a local Qwen2.5-1.5B (llama.cpp, CPU) from a prompt that
contains only the retrieved records, and the whole reply is rendered in the
caller's language by the local IndicTrans2 translator.

Two release rules are enforced here rather than left to the model:

* live source text is never placed in the prompt.  Live rows enter the prompt as
  provenance metadata only, because the platform does not publish live text
  while multilingual PERSON redaction recall is unvalidated.  Synthetic fixture
  text is inspectable and is passed in full;
* every model-backed capability degrades to the previous deterministic summary
  when its model is not downloaded, and says so in ``capabilities``.
"""

