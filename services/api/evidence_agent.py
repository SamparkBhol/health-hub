@functools.lru_cache(maxsize=1)
def _source_titles() -> dict[str, str]:
    """Registered document titles by source id.

    A registry title ("Odisha Health and Family Welfare circulars (Odia)") is
    reviewed platform metadata, not crawled text, so it is safe to embed where
    the document body is not, and it is the only content-bearing string a live
    row has.
    """

    try:
        from workers.ingestion.registry import load_registry  # noqa: PLC0415

        return {source.id: source.name for source in load_registry().sources}
    except Exception:  # noqa: BLE001 - retrieval degrades, it never fails a request
        return {}


@dataclass(slots=True)
class EvidenceAgent:
    database: Database
    gazetteer: DistrictGazetteer
    diseases: DiseaseLexicon

    @classmethod
    def create(cls, database: Database) -> EvidenceAgent:
        return cls(
            database=database,
            gazetteer=DistrictGazetteer.load(),
            diseases=DiseaseLexicon.load(),
        )

    # ------------------------------------------------------------------
    # public entry point
    # ------------------------------------------------------------------
    def answer(
        self,
        question: str,
        *,
        district_id: str | None,
        disease: str | None,
        maximum_evidence: int,
        target_language: str | None = None,
        history: list[dict[str, str]] | None = None,
    ) -> dict[str, Any]:
        detected = route_unicode(question).value
        question_language = detected if detected in {"en", "hi", "or"} else "en"
        requested = (target_language or "").strip().lower()
        answer_language = requested if requested in {"en", "hi", "or"} else question_language

        english_question = self._to_english(question, question_language)
        resolved_districts = self.gazetteer.resolve(question)
        resolved_diseases = self.diseases.find(question)
        if english_question != question:
            # A translated question exposes district and disease names the native
            # gazetteer spelling may have missed (and vice versa); both are used.
            resolved_districts = resolved_districts + self.gazetteer.resolve(english_question)
            resolved_diseases = tuple(resolved_diseases) + tuple(
                self.diseases.find(english_question)
            )
        distinct_districts = tuple(dict.fromkeys(match.district_id for match in resolved_districts))
        distinct_diseases = tuple(dict.fromkeys(resolved_diseases))

        # Follow-up context is intentionally narrow. Only prior USER questions
        # may provide a missing district or disease; generated assistant text is
        # never fed back as a fact. The newest unambiguous user turn wins.
        prior_user_turns = [
            str(turn.get("content", "")).strip()
            for turn in (history or [])
            if turn.get("role") == "user" and str(turn.get("content", "")).strip()
        ]
        historical_district: str | None = None
        historical_disease: str | None = None
        for prior in reversed(prior_user_turns):
            if historical_district is None:
                matches = tuple(
                    dict.fromkeys(match.district_id for match in self.gazetteer.resolve(prior))
                )
                if len(matches) == 1:
                    historical_district = matches[0]
            if historical_disease is None:
                matches = tuple(dict.fromkeys(self.diseases.find(prior)))
                if len(matches) == 1:
                    historical_disease = matches[0]
            if historical_district is not None and historical_disease is not None:
                break
        selected_district = district_id or (
            distinct_districts[0] if len(distinct_districts) == 1 else historical_district
        )
        selected_disease = disease or (
            distinct_diseases[0] if len(distinct_diseases) == 1 else historical_disease
        )
        intent = self._intent(
            question.casefold(),
            has_disease=bool(disease or distinct_diseases),
        )
        if intent == "evidence_search" and english_question != question:
            intent = self._intent(
                english_question.casefold(),
                has_disease=bool(disease or distinct_diseases),
            )
        # A follow-up carries scope but usually not intent: "What about Koraput?"
        # after a forecast question is still a forecast question, and answering it
        # with an evidence search silently changed the subject.  Intent is
        # inherited only when the current question states none of its own.
        inherited_intent = False
        if (
            intent == "evidence_search"
            and prior_user_turns
            and not self._carries_intent_terms(question)
            and not self._carries_intent_terms(english_question)
        ):
            for prior in reversed(prior_user_turns):
                previous = self._intent(
                    prior.casefold(),
                    has_disease=bool(self.diseases.find(prior)) or bool(selected_disease),
                )
                if previous != "evidence_search":
                    intent = previous
                    inherited_intent = True
                    break
        scope = {
            "district_id": selected_district,
            "disease": selected_disease,
            "question_language": question_language,
            "conversation_context_used": bool(
                prior_user_turns
                and (
                    historical_district is not None
                    or historical_disease is not None
                    or inherited_intent
                )
            ),
            "intent_inherited_from_history": inherited_intent,
        }

        ambiguous_district = district_id is None and len(distinct_districts) > 1
        ambiguous_disease = disease is None and len(distinct_diseases) > 1
        # Naming two districts is only ambiguous when the answer must pick one.
        # An official-statistics question can report both from the annual table,
        # so refusing it there would hide data the platform already holds. A
        # second disease stays ambiguous: the official series is per-disease.
        if intent == "incidence_request" and not ambiguous_disease:
            ambiguous_district = False
        if ambiguous_district or ambiguous_disease:
            return self._deterministic(
                intent=intent,
                answer_state="ambiguous_scope",
                english_answer=AMBIGUOUS_ANSWER,
                scope=scope,
                reason_codes=["AMBIGUOUS_QUERY_SCOPE"],
                answer_language=answer_language,
                question_language=question_language,
                extra={
                    "scope_candidates": {
                        "district_ids": list(distinct_districts),
                        "diseases": list(distinct_diseases),
                    }
                },
            )

        if intent == "clinical_advice_request":
            return self._deterministic(
                intent=intent,
                answer_state="out_of_scope_clinical",
                english_answer=CLINICAL_ANSWER,
                scope=scope,
                reason_codes=["CLINICAL_ADVICE_OUT_OF_SCOPE"],
                answer_language=answer_language,
                question_language=question_language,
            )

        # A place the gazetteer cannot resolve is not a licence to answer from
        # whichever Odisha rows happen to match the disease: "malaria cases in
        # Bhopal" was answered with Odisha records and read as a Bhopal figure.
        if district_id is None and not distinct_districts:
            outside = self._place_outside_odisha(question, english_question)
            if outside is not None:
                return self._deterministic(
                    intent=intent,
                    answer_state="district_not_in_odisha",
                    english_answer=OUTSIDE_ODISHA_ANSWER.format(place=outside),
                    scope=scope,
                    reason_codes=["PLACE_OUTSIDE_ODISHA_COVERAGE"],
                    answer_language=answer_language,
                    question_language=question_language,
                    extra={"unresolved_place": outside},
                )
        if intent == "forecast_request":
            return self._forecast_outlook(
                scope=scope,
                english_question=english_question,
                selected_district=selected_district,
                selected_disease=selected_disease,
                answer_language=answer_language,
                question_language=question_language,
            )
        if intent == "incidence_request":
            public_observation = self._public_malaria_observation(
                scope=scope,
                selected_district=selected_district,
                selected_disease=selected_disease,
                answer_language=answer_language,
                question_language=question_language,
                comparison_districts=list(distinct_districts),
                wants_trend=any(
                    term in english_question.casefold() for term in TREND_TERMS
                ),
                wants_ranking=any(
                    term in english_question.casefold() for term in RANKING_TERMS
                ),
                requested_years=self._requested_years(english_question),
            )
            if public_observation is not None:
                return public_observation
            return self._deterministic(
                intent=intent,
                answer_state="not_observable_from_public_sources",
                english_answer=INCIDENCE_ANSWER,
                scope=scope,
                reason_codes=["NOT_INCIDENCE", "AUTHORISED_SURVEILLANCE_NOT_SUPPLIED"],
                answer_language=answer_language,
                question_language=question_language,
            )
        if intent == "data_audit":
            return self._deterministic(
                intent=intent,
                answer_state="audited_public_catalogue",
                english_answer=AUDIT_ANSWER,
                scope=scope,
                reason_codes=["POSITIVE_ONLY_CATALOGUE", "TARGET_SERIES_INELIGIBLE"],
                answer_language=answer_language,
                question_language=question_language,
            )

        if intent == "verified_events":
            events = self.database.list_verified_events(
                limit=maximum_evidence,
                district_id=selected_district,
                disease=selected_disease,
            )
            english = (
                f"Found {len(events)} active reviewer-verified event record(s). "
                "Reviewer verification is not the same as official surveillance."
            )
            payload = self._deterministic(
                intent=intent,
                answer_state="records_returned" if events else "insufficient_evidence",
                english_answer=english,
                scope=scope,
                reason_codes=[] if events else ["NO_VERIFIED_EVENT_IN_CURRENT_STORE"],
                answer_language=answer_language,
                question_language=question_language,
            )
            payload["evidence"] = events
            payload["is_synthetic"] = any(bool(item.get("is_fixture")) for item in events)
            return payload

        return self._evidence_answer(
            question=question,
            english_question=english_question,
            prior_user_turns=prior_user_turns,
            intent=intent,
            scope=scope,
            selected_district=selected_district,
            selected_disease=selected_disease,
            maximum_evidence=maximum_evidence,
            question_language=question_language,
            answer_language=answer_language,
        )

    # ------------------------------------------------------------------
    # retrieval + generation
    # ------------------------------------------------------------------
    def _forecast_outlook(
        self,
        *,
        scope: dict[str, Any],
        english_question: str,
        selected_district: str | None,
        selected_disease: str | None,
        answer_language: str,
        question_language: str,
    ) -> dict[str, Any]:
        """Answer from a qualified disease model, otherwise from environmental context.

        The authorised model is attempted only when the question resolves to a
        disease. A missing/unqualified/stale model falls through to the current
        environmental priority layer, preserving a useful answer without
        relabelling weather context as disease probability.
        """

        horizon = self._forecast_horizon(english_question)
        if selected_disease:
            try:
                from packages.forecasting.operational import current_probability_map

                forecast = current_probability_map(
                    disease=selected_disease,
                    horizon_weeks=horizon,
                )
            except Exception:  # noqa: BLE001 - environmental fallback is the public contract
                forecast = None
            if forecast and forecast.get("status") == "published":
                districts = list(forecast.get("districts") or [])
                district_row = next(
                    (row for row in districts if row.get("district_id") == selected_district),
                    None,
                )
                if district_row is not None:
                    probability = float(district_row["probability_threshold_exceedance"])
                    threshold = float(district_row["outbreak_threshold_per_100k"])
                    completeness = float(district_row["latest_case_volume_completeness"])
                    district_name = str(selected_district).removeprefix("OD-DIST-")
                    english = (
                        f"For {district_name.replace('-', ' ').title()}, the qualified "
                        f"{horizon}-week {selected_disease.replace('_', ' ')} model gives "
                        f"a {probability * 100:.1f}% probability of crossing the registered "
                        f"threshold of {threshold:g} cases per 100,000. The latest case-volume "
                        f"completeness is {completeness * 100:.0f}%. Review the model drivers "
                        "and local evidence before escalation."
                    )
                    answer_rows = [district_row]
                else:
                    ranked = sorted(
                        districts,
                        key=lambda row: float(row["probability_threshold_exceedance"]),
                        reverse=True,
                    )[:5]
                    summary = ", ".join(
                        f"{self._district_display(str(row['district_id']))} "
                        f"{float(row['probability_threshold_exceedance']) * 100:.1f}%"
                        for row in ranked
                    )
                    english = (
                        f"The highest qualified {horizon}-week "
                        f"{selected_disease.replace('_', ' ')} "
                        f"threshold-exceedance probabilities are {summary}. Each value uses the "
                        "registered disease threshold, current environment and authorised "
                        "as-of surveillance history."
                    )
                    answer_rows = ranked
                payload = self._deterministic(
                    intent="forecast_request",
                    answer_state="forecast_probability",
                    english_answer=english,
                    scope=scope,
                    reason_codes=[
                        "QUALIFIED_OPERATIONAL_FORECAST",
                        "HUMAN_VERIFICATION_REQUIRED",
                    ],
                    answer_language=answer_language,
                    question_language=question_language,
                    generation_mode="policy_response",
                )
                payload["forecast"] = {
                    "issue_week": forecast.get("issue_week"),
                    "horizon_weeks": horizon,
                    "disease": selected_disease,
                    "quantity": forecast.get("quantity"),
                    "districts": answer_rows,
                    "is_synthetic": False,
                }
                return payload

        # Public-data predictive analysis is a separate, deliberately narrower
        # contract.  It is useful for malaria planning questions even when the
        # authorised routine-surveillance model is unavailable: the target is
        # elevated monthly HMIS microscopy positivity, not an outbreak.  The
        # serving artefact is selected strictly by rolling-origin Brier score.
        if selected_disease == "malaria":
            public = self._public_malaria_outlook(
                scope=scope,
                english_question=english_question,
                selected_district=selected_district,
                answer_language=answer_language,
                question_language=question_language,
            )
            if public is not None:
                return public

        try:
            from packages.forecasting.current_conditions import current_conditions_map

            layer = current_conditions_map()
        except Exception:  # noqa: BLE001 - typed fallback is the public contract
            return self._deterministic(
                intent="forecast_request",
                answer_state="insufficient_evidence",
                english_answer=FORECAST_ANSWER,
                scope=scope,
                reason_codes=["CURRENT_ENVIRONMENTAL_LAYER_UNAVAILABLE"],
                answer_language=answer_language,
                question_language=question_language,
            )

        observed = [
            row
            for row in layer.get("districts", [])
            if row.get("status") == "observed"
            and isinstance(row.get("suitability_percentile"), (int, float))
        ]
        district_row = next(
            (row for row in observed if row.get("district_id") == selected_district),
            None,
        )
        disease_label = (selected_disease or "public-health events").replace("_", " ")
        if selected_district and district_row is None:
            english = (
                f"No current environmental index is available for {selected_district}. "
                "The system did not substitute a neighbouring district or a statewide average."
            )
            payload = self._deterministic(
                intent="forecast_request",
                answer_state="insufficient_evidence",
                english_answer=english,
                scope=scope,
                reason_codes=["DISTRICT_ENVIRONMENTAL_WINDOW_INCOMPLETE"],
                answer_language=answer_language,
                question_language=question_language,
            )
            payload["outlook"] = {"as_of": layer.get("as_of"), "district": None}
            return payload

        if district_row is not None:
            name = str(district_row.get("canonical_name") or selected_district)
            percentile = float(district_row["suitability_percentile"])
            band = str(district_row.get("band") or "unclassified").replace("_", " ")
            drivers = [
                str(item.get("feature", "")).replace("_", " ")
                for item in district_row.get("top_drivers", [])[:3]
                if isinstance(item, dict) and item.get("feature")
            ]
            driver_text = ", ".join(drivers) or "the available weather features"
            warning = str(
                district_row.get("imd_peak_warning_next_5_days") or "not reported"
            ).replace("_", " ")
            english = (
                f"For {name}, the current environmental early-warning index is at the "
                f"{percentile:.1f} percentile of that district's historical seasonal "
                f"conditions ({band}) for {disease_label}. The main drivers are "
                f"{driver_text}; the five-day IMD warning level is {warning}. Use this "
                "to prioritise evidence review and field verification."
            )
            outlook_rows = [district_row]
        else:
            ranked = sorted(
                observed,
                key=lambda row: float(row["suitability_percentile"]),
                reverse=True,
            )[:5]
            summary = ", ".join(
                f"{row.get('canonical_name')} {float(row['suitability_percentile']):.1f}"
                for row in ranked
            )
            english = (
                "The districts with the highest current environmental early-warning "
                f"indices are {summary}. These values rank weather-related verification "
                "priority; open a district to inspect rainfall, temperature, humidity "
                "and IMD warning drivers."
            )
            outlook_rows = ranked

        payload = self._deterministic(
            intent="forecast_request",
            answer_state="risk_factor_outlook",
            english_answer=english,
            scope=scope,
            reason_codes=[
                "ENVIRONMENTAL_RELATIVE_RISK_INDEX",
                "HUMAN_VERIFICATION_REQUIRED",
                "CALIBRATED_DISEASE_PROBABILITY_REQUIRES_AUTHORISED_SURVEILLANCE",
            ],
            answer_language=answer_language,
            question_language=question_language,
            generation_mode="policy_response",
        )
        payload["outlook"] = {
            "as_of": layer.get("as_of"),
            "quantity": layer.get("quantity"),
            "districts": outlook_rows,
            "is_synthetic": False,
        }
        return payload

